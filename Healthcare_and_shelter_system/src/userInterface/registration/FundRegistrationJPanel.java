    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        // TODO add your handling code here:
        if(txtDonarName.getText().isEmpty() | txtLocation.getText().isEmpty()|txtEmail.getText().isEmpty()|txtUser.getText().isEmpty()|txtPassword.getText().isEmpty()|txtContactNo.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Please fill all the fields", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        int userCheck =0;
        String userName =txtUser.getText();
        String pass = txtPassword.getText();
        UserAccount userAccount = system.getUserAccountDirectory().authenticateUser(userName, pass);
        if (userAccount == null) {
            for (Network network : system.getNetworkList()) {
                //Step 2.a: check against each enterprise
                for (Enterprise enterprise : network.getEnterpriseDirectory().getEnterpriseList()) {
                    for (Organization organization : enterprise.getOrganizationDirectory().getOrganizationList()) {
                        System.out.println(organization);
                        userAccount = organization.getUserAccountDirectory().authenticateUser(userName, pass);
                        System.out.println(userAccount);
                        if (userAccount != null) {
                            userCheck++;
                            System.out.println(userAccount);
                            // break;
                        }
                    }
                }
            }
        }
        if (userCheck > 0) {
            JOptionPane.showMessageDialog(this, "User name already exists!");
        } else if (userCheck == 0) {
            SponsorApprovalStatus request = new SponsorApprovalStatus();
            request.setTypeOfRequest("Sponsor");
                request.setDonarName(txtDonarName.getText());
                request.setUsername(userName);
                request.setPassword(pass);
                request.setMobile(txtContactNo.getText());
                request.setStatus("Pending");
                String selectedString = (String) donarRole.getSelectedItem();
                if(selectedString.equalsIgnoreCase("Corporate")){
                    request.setRole("Corporate");
                } else if(selectedString.equalsIgnoreCase("General")){
                    request.setRole("General");
                } else {
                    JOptionPane.showMessageDialog(this, "Select form the options provided");
                }
                
                Optional<Network> network;
                network = system.getNetworkList().stream()
                        .filter(x -> x.getName().equalsIgnoreCase(txtLocation.getText()))
                        .findFirst();
                Network currentNetwork = network.get();
                for (Enterprise ent : currentNetwork.getEnterpriseDirectory().getEnterpriseList()) {
                if (ent.enterpriseType.equalsIgnoreCase(Enterprise.EnterpriseType.FundRaiser.getValue())) {
                    for(UserAccount adminUser : ent.getUserAccountDirectory().getUserAccountList()) {
                        if(adminUser.getRole() instanceof AdminRole) {
                            adminUser.getWorkQueue().getWorkRequestList().add(request);
                            JOptionPane.showMessageDialog(this, "Registration submitted Successfully. Sent to Fund Admin for Approval!");

                        }
                    }
                }
            }
                    
        }
    }//GEN-LAST:event_btnSubmitActionPerformed


